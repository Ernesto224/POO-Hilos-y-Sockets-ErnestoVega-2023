    public void abrirAreaDeJuego() {
        if (this.ventanaDeJuego == null) {
            this.ventanaDeJuego = new JfVentanaDeJuego();
            this.ventanaDeJuego.setVisible(true);
            ClienteSingleton.getInstance().enviarDatos(GestionXML.xmlToString(GestionXML.crearMensajeProtocolo(Solicitud.SOLICITAR_OPONENTES.name())));
        }//valida que solo se desplegue una ventana de juego a la vez
    } //habre una nueva ventana de juego

    public void cargarSolicitudes(String[] solicitudes) {
        if (this.solicitudesResibidas != null) {
            this.solicitudesResibidas.cargarSolicitudes(solicitudes);
        }
    }//carga las solicitudes de amistad guardadas del usuario

    public void desplegarJugadorPerdio() {
        this.ventanaDeJuego.dispose();
        this.ventanaDeJuego=null;
        JIFJugadorPerdio jIFJugadorPerdio = new JIFJugadorPerdio();
        this.jDesktopPane1.add(jIFJugadorPerdio);
        jIFJugadorPerdio.setVisible(true);
    }//cierra la ventana de juego y despliega un mensaje de jugador perdio

    public void desplegarJugadorGano(Jugador jugador,ArrayList<Oponente> oponentes) {
        this.ventanaDeJuego.dispose();
        this.ventanaDeJuego=null;
        JIFJugadorGano jIFJugadorGano = new JIFJugadorGano(jugador, oponentes);
        this.jDesktopPane1.add(jIFJugadorGano);
        jIFJugadorGano.setVisible(true);
    }//abre la ventana de victoria

    public void desplagarRanking(String[] usuariosRanking) {
        this.ranking = new JFRanking();
        this.ranking.cargarRanking(usuariosRanking);
        this.ranking.setLocationRelativeTo(this);
        this.ranking.setVisible(true);
    }//desplaga el ranking de juago

    public void cargarAmigos(String[] amigos, String[] mapas) {
        if (this.inicarPartida != null) {
            this.inicarPartida.cargarInfoAmigos(amigos);
            this.inicarPartida.cargarInfoMapas(mapas);
        }
    }//carga la lista de amigos de unjugador

    public void agregarMuro(String publicacion) {
        if (this.muro != null) {
            this.muro.agregarMensaje(publicacion);
        }
    }//llama a la ventana del muro para eagregar una publicaion

